function myFunction(){
  var x = document.getElementById("pasd");
  var y = document.getElementById("eyev");
  var z = document.getElementById("eyeslash");

  if(x.type === 'password'){
    x.type = "text";
    y.style.display = "block";
    z.style.display = "none";
  }
  else{
    x.type = "password";
    y.style.display = "none";
    z.style.display = "block";
  }
}

